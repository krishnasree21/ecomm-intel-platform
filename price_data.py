def find_product_price(product_name: str):
    product_lower = product_name.lower().strip()
    
    # Exact match
    if product_lower in PRODUCT_PRICES:
        return PRODUCT_PRICES[product_lower]
    
    # Partial match — key contains search term
    for key, value in PRODUCT_PRICES.items():
        if product_lower in key or key in product_lower:
            return value
    
    # Word match — any significant word matches
    words = [w for w in product_lower.split() if len(w) > 3]
    for key, value in PRODUCT_PRICES.items():
        key_words = key.split()
        if any(word in key_words for word in words):
            return value
    
    return None